const findKey = function(object, callback) {
  let result;

  for (let keys in object) {
    //console.log("object[keys]", object[keys])
    if (callback(object[keys])) {
      result = keys;
      //console.log(result);
      return result;
    }
  }
  //console.log("result", result);
  return result;
};

